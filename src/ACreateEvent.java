private void populateClubCombo() {
    try (Connection con = DBConnection.getConnection()) { // Use your DBConnection class
        String sql = "SELECT DISTINCT club FROM events";
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery(sql);
        
        club.removeAllItems(); // Clear existing items
        club.addItem("-- Select Club --"); // Add default option
        
        while(rs.next()) {
            club.addItem(rs.getString("club"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading clubs: " + e.getMessage());
    }
}

private void populateTypeCombo() {
    try (Connection con = DBConnection.getConnection()) {
        String sql = "SELECT DISTINCT type FROM events";
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery(sql);
        
        type.removeAllItems(); // Clear existing items
        type.addItem("-- Select Type --"); // Add default option
        
        while(rs.next()) {
            type.addItem(rs.getString("type"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading event types: " + e.getMessage());
    }
}

