    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
String eventName1Str = eventName1.getText();
String club1 = (String) club.getSelectedItem();
        String type1 = (String) type.getSelectedItem();
        Date date1 = date.getDate();
        String venue1 = venue.getText();

        int seats1;
        try {
            seats1 = Integer.parseInt(seats.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for seats.");
            return;
        }
        String description1 = description.getText();

        if (eventName1Str.isEmpty() || club1.isEmpty() || type1.isEmpty() || date1 == null || venue1.isEmpty() || description1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields.");
            return;
        }

        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/events", "root", "Root@123")) {
            String sql = "INSERT INTO events (name, club, type, date, venue, seats, description) VALUES (?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pstmt = conn.prepareStatement(sql);
pstmt.setString(1, eventName1Str);
            pstmt.setString(2, club1);
            pstmt.setString(3, type1);
            pstmt.setString(4, new java.text.SimpleDateFormat("yyyy-MM-dd").format(date1));
            pstmt.setString(5, venue1);
            pstmt.setInt(6, seats1);
            pstmt.setString(7, description1);
            pstmt.executeUpdate();

            JOptionPane.showMessageDialog(this, "Event created successfully!");
            this.dispose();
            new AdminDashboard().setVisible(true); // Go back to admin dashboard
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

